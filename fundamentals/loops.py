"""
File: loops.py
Description: Demonstrates the use of for and while loops in Python.
"""

# For Loop
print("For Loop Example")
for i in range(1, 6):
    print("Iteration:", i)

print("\n-----------------------")

# While Loop
print("While Loop Example")
count = 1

while count <= 5:
    print("Count:", count)
    count += 1