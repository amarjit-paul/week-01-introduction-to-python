"""
File: operators.py
Description: Demonstrates the use of different types of operators in Python.
"""

# Arithmetic Operators
a = 10
b = 3

print("Arithmetic Operators")
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)

print("\n-----------------------")

# Comparison Operators
print("Comparison Operators")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)

print("\n-----------------------")

# Logical Operators
x = True
y = False

print("Logical Operators")
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)

print("\n-----------------------")

# Assignment Operators
c = 5

print("Assignment Operators")
print("Initial value:", c)

c += 2
print("After += 2:", c)

c -= 1
print("After -= 1:", c)

c *= 3
print("After *= 3:", c)

c /= 2
print("After /= 2:", c)