"""
File: data_types.py
Description: Demonstrates the basic data types in Python.
"""

# Integer
age = 20

# Float
height = 5.8

# String
name = "Alice"

# Boolean
is_student = True

# List
subjects = ["Python", "Math", "Cloud Computing"]

# Tuple
coordinates = (10, 20)

# Dictionary
student = {
    "name": "Alice",
    "age": 20,
    "course": "Python"
}

# Set
colors = {"Red", "Green", "Blue"}

# Display values and their data types
print("Age:", age, "| Type:", type(age))
print("Height:", height, "| Type:", type(height))
print("Name:", name, "| Type:", type(name))
print("Is Student:", is_student, "| Type:", type(is_student))
print("Subjects:", subjects, "| Type:", type(subjects))
print("Coordinates:", coordinates, "| Type:", type(coordinates))
print("Student:", student, "| Type:", type(student))
print("Colors:", colors, "| Type:", type(colors))